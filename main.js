// Типи змінних:
// let - можуть міняти значення поки програма виконується
// var - аналог let, тільки старіший
// const - змінна яка не може міняти значення

// Приклад роботи змінної "let":
let userName = 'Ihor'; // Початкове значення змінної
userName = 'Nazar'; // Значення змінної змінилось
console.log(userName);

// Приклад роботи змінної "Const":
const yearOfBirt = 2009;
console.log(yearOfBirt);

// Приклад роботи змінної "Var":
var city = 'London';
city = 'Kyiv';
city = 'Lviv'
console.log(city)

// Також можна ще так створювати змінні:
let name, age;
name = "Ihor";
